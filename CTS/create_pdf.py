from fpdf import FPDF

class PDF(FPDF):
    def header(self):
        self.set_font('Arial', 'B', 12)
        self.cell(0, 10, 'PDF Document Title', 0, 1, 'C')

    def footer(self):
        self.set_y(-15)
        self.set_font('Arial', 'I', 8)
        self.cell(0, 10, f'Page {self.page_no()}', 0, 0, 'C')

    def chapter_title(self, title):
        self.set_font('Arial', 'B', 12)
        self.cell(0, 10, title, 0, 1, 'L')
        self.ln(10)

    def chapter_body(self, body):
        self.set_font('Arial', '', 12)
        self.multi_cell(0, 10, body)
        self.ln()

pdf = PDF()
pdf.add_page()
pdf.set_title('Sample PDF')
pdf.set_author('Your Name')

pdf.chapter_title('Introduction')
pdf.chapter_body('This is a simple PDF created with Python and fpdf library.')

pdf.chapter_title('Section 1')
pdf.chapter_body('Here we have another section with some more text.')

pdf.output('simple_pdf.pdf')
